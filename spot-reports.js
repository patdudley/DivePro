function buildChart(base, key) {
  return Array.from({ length: 24 }, (_, hour) => ({
    time: `${String(hour).padStart(2, "0")}:00`,
    [key]: Math.max(0.2, Number((base + Math.sin(hour / 3) * 0.35 + (hour > 13 && hour < 19 ? 0.4 : 0)).toFixed(2))),
  }));
}

